# Логические операции >, <, >=, <=, ==, !=, not, and, or, is, is not, in, not in
a = 1 > 2            # Ответ: False
b = 1 < 2            # Ответ: True
c = 1 < 2 and 4 > 3  # Ответ: True
d = 1 < 3 < 5        # Ответ: True, можно использовать тройные неравенства   
e = [1, 2, 3]
f = 2 in e           # Ответ: True
g = not 2 in e       # Ответ: False
h = e[0] % 2 == 0    # Ответ: False, проверка числа на четность (можно записать h = not e[0] % 2)














